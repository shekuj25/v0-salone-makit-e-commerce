// Helper functions
export function getCategoryById(id: string): Category | undefined {
  return mockCategories.find((cat) => cat.id === id)
}

export function getCategoryBySlug(slug: string): Category | undefined {
  return mockCategories.find((cat) => cat.slug === slug)
}

export function getProductsByCategory(categoryId: string): Product[] {
  return mockProducts.filter((product) => product.category_id === categoryId && product.is_active)
}

export function getFeaturedProducts(): Product[] {
  return mockProducts.filter((product) => product.is_featured && product.is_active).slice(0, 8)
}

export function getProductById(id: string): Product | undefined {
  return mockProducts.find((product) => product.id === id)
}

export function searchProducts(query: string): Product[] {
  const lowerQuery = query.toLowerCase()
  return mockProducts.filter(
    (product) =>
      product.is_active &&
      (product.name_en.toLowerCase().includes(lowerQuery) ||
        product.name_krio.toLowerCase().includes(lowerQuery) ||
        product.description_en.toLowerCase().includes(lowerQuery) ||
        product.description_krio.toLowerCase().includes(lowerQuery)),
  )
}
